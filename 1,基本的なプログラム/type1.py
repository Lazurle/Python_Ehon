a = 1
print(type(a))
#出力結果： <class 'int'>

a = 100000000000
print(type(a))
#出力結果：<class 'int'>
#長整数型(long)はint型に統合された。

a = 1.23
print(type(a))
#出力結果：<class 'float'>

a = 'Hello World!'
print(type(a))
#出力結果： <class 'str'>

a = True
print(type(a))
#出力結果：<class 'bool'>

a = [1, 2, 3]
print(type(a))
#出力結果：<class 'list'>

a = (1, 2, 3)
print(type(a))
#出力結果：<class 'tuple'>
